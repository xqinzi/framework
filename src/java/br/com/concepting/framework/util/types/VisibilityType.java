package br.com.concepting.framework.util.types;

/**
 * Classe que define as constantes dos modos de visibilidade de um objeto/componente.
 * 
 * @author fvilarinho
 * @since 3.0
 */
public enum VisibilityType{
    /**
     * Constante que define que o objeto/componente está escondido (não visível).
     */
    HIDDEN,
    
    /**
     * Constante que define que o objeto/componente está visível.
     */
    VISIBLE;
}
