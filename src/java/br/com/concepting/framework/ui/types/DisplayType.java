package br.com.concepting.framework.ui.types;
 
/**
 * Classe que define as constantes dos modos de renderização de um objeto/componente.
 * 
 * @author fvilarinho
 * @since 3.0
 */
public enum DisplayType{
    /**
     * Constante que define que o objeto/componente não está renderizado.
     */
    NONE,
    
    /**
     * Constante que define que o objeto/componente será renderizado sem quebra de linha..
     */
    NOWRAP;
}
